sub process {
  local $_=shift;
  my $ugr=substr($_,4,4);
  return "ihr:PrUGr$ugr ihr:hasChild ihr:Bezug$_ .\n";
}

sub fix() {
  local $_=shift;
  s/\.//g;
  return $_;
}

